    /**
 * Carga en el JComboBox todas las casas que no tienen propietario
 * asignado actualmente.
 *
 * Si no existen viviendas disponibles, deshabilita el botón de
 * registro e informa al usuario mediante la lista desplegable.
 */
    private void cargarCasasDisponibles() {
        jComboBox1.removeAllItems();
        jComboBox1.addItem("Seleccione una casa");
        String sql = "SELECT numero_casa FROM Casas WHERE id_propietario IS NULL ORDER BY numero_casa";
        try (Connection con = ConexionDB.getConexion();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(sql)) {
            while (rs.next()) {
                jComboBox1.addItem(String.valueOf(rs.getInt("numero_casa")));
            }
        } catch (SQLException e) {
            mostrarError("Error al cargar casas: " + e.getMessage());
        }
        if (jComboBox1.getItemCount() == 1) {
            jComboBox1.removeAllItems();
            jComboBox1.addItem("Sin casas disponibles");
            jButton1.setEnabled(false);
        } else {
            jButton1.setEnabled(true);
        }
    }

    
    /**
 * Verifica si un correo electrónico cumple con una validación básica.
 *
 * La validación comprueba que exista un símbolo '@' y que el dominio
 * contenga al menos un punto.
 *
 * @param correo dirección de correo electrónico a validar.
 * @return true si el formato es válido; false en caso contrario.
 */
    private boolean correoEsValido(String correo) {
        int arroba = correo.indexOf('@');
        if (arroba <= 0) return false;
        String dominio = correo.substring(arroba + 1);
        return dominio.contains(".") && !dominio.endsWith(".");
    }


