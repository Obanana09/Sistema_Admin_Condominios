    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre   = jTextField1.getText().trim();
        String telefono = jTextField2.getText().trim();
        String correo   = jTextField3.getText().trim();
        String casaSel  = (String) jComboBox1.getSelectedItem();

        if (nombre.isEmpty()) {
            mostrarError("El campo Nombre Completo es obligatorio.");
            jTextField1.requestFocus();
            return;
        }
        if (telefono.isEmpty()) {
            mostrarError("El campo Teléfono es obligatorio.");
            jTextField2.requestFocus();
            return;
        }
        if (!telefono.matches("\\d+")) {
            mostrarError("El teléfono solo puede contener números.\nEjemplo: 12345678");
            jTextField2.requestFocus();
            return;
         }
        if (correo.isEmpty()) {
            mostrarError("El campo Correo Electrónico es obligatorio.");
            jTextField3.requestFocus();
            return;
        }
        if (casaSel == null || casaSel.equals("Sin casas disponibles")) {
            mostrarError("No hay casas disponibles para registrar.");
            return;
        }
        if (casaSel.equals("Seleccione una casa")) {
            mostrarError("Selecciona una casa válida.");
            return;
        }
        if (!correoEsValido(correo)) {
            mostrarError("El correo electrónico no tiene un formato válido.\nEjemplo: usuario@dominio.com");
            jTextField3.requestFocus();
            return;
        }

        int numeroCasa = Integer.parseInt(casaSel);

        String sqlCheck = "SELECT id_propietario FROM Casas WHERE numero_casa = ?";
        try (Connection con = ConexionDB.getConexion();
             PreparedStatement ps = con.prepareStatement(sqlCheck)) {
            ps.setInt(1, numeroCasa);
            ResultSet rs = ps.executeQuery();
            if (rs.next() && rs.getObject("id_propietario") != null) {
                mostrarError("La casa " + numeroCasa + " ya tiene un propietario registrado.");
                cargarCasasDisponibles();
                // Solo permitir números en teléfono
        jTextField2.addKeyListener(new java.awt.event.KeyAdapter() {
        @Override
        public void keyTyped(java.awt.event.KeyEvent e) {
        char c = e.getKeyChar();
        if (!Character.isDigit(c)) {
            e.consume(); // bloquea cualquier carácter que no sea número
        }
       }
    });
                return;
            }
        } catch (SQLException e) {
            mostrarError("Error al verificar la casa: " + e.getMessage());
            return;
        }

        String sqlInsert = "INSERT INTO Propietarios (nombre, telefono, correo) VALUES (?, ?, ?)";
        String sqlUpdate = "UPDATE Casas SET id_propietario = ? WHERE numero_casa = ?";

        try (Connection con = ConexionDB.getConexion()) {
            con.setAutoCommit(false);
            int idNuevo;
            try (PreparedStatement ps = con.prepareStatement(sqlInsert, Statement.RETURN_GENERATED_KEYS)) {
                ps.setString(1, nombre);
                ps.setString(2, telefono);
                ps.setString(3, correo);
                ps.executeUpdate();
                ResultSet keys = ps.getGeneratedKeys();
                keys.next();
                idNuevo = keys.getInt(1);
            }
            try (PreparedStatement ps = con.prepareStatement(sqlUpdate)) {
                ps.setInt(1, idNuevo);
                ps.setInt(2, numeroCasa);
                ps.executeUpdate();
            }
            con.commit();
            JOptionPane.showMessageDialog(this,
                    "Propietario registrado exitosamente.\n\n"
                  + "  Nombre:   " + nombre     + "\n"
                  + "  Casa:     " + numeroCasa  + "\n"
                  + "  Telefono: " + telefono   + "\n"
                  + "  Correo:   " + correo,
                    "Registro exitoso",
                    JOptionPane.INFORMATION_MESSAGE);
            limpiarFormulario();
            cargarCasasDisponibles();
        } catch (SQLException e) {
            mostrarError("Error al registrar propietario: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

