    /**
 * Registra un nuevo pago mensual.
 *
 * El proceso incluye:
 *
 * 1. Validar que los datos estén completos.
 * 2. Verificar que la casa tenga propietario.
 * 3. Evitar pagos duplicados.
 * 4. Validar la secuencia de meses.
 * 5. Obtener el correo del propietario.
 * 6. Registrar el pago en la base de datos.
 * 7. Enviar una confirmación por correo.
 * 8. Confirmar la transacción.
 *
 * Si ocurre algún error, el pago no se guarda.
 *
 * @param evt evento generado al presionar el botón
 *            "Registrar Pago".
 */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String casaSel  = (String) jComboBox1.getSelectedItem();
        String mesSel   = (String) jComboBox2.getSelectedItem();
        String anioSel  = (String) jComboBox3.getSelectedItem();

        if (casaSel == null || mesSel == null || anioSel == null
                || casaSel.equals("Seleccione una casa")
                || mesSel.equals("Seleccione un mes")
                || anioSel.equals("Seleccione un año")) {
            mostrarError("Selecciona la casa, el mes y el año antes de registrar.");
            return;
        }

        int numeroCasa = Integer.parseInt(casaSel);
        int anio       = Integer.parseInt(anioSel);

        // Convertir mes a número
        String[] meses = {"Enero","Febrero","Marzo","Abril","Mayo","Junio",
                          "Julio","Agosto","Septiembre","Octubre","Noviembre","Diciembre"};
        int numeroMes = java.util.Arrays.asList(meses).indexOf(mesSel.trim()) + 1;
        
         // Validar que la casa tenga propietario
        String sqlCasa = "SELECT id_propietario FROM Casas WHERE numero_casa = ?";
        try (java.sql.Connection con = ConexionDB.getConexion();
             java.sql.PreparedStatement ps = con.prepareStatement(sqlCasa)) {
            ps.setInt(1, numeroCasa);
            java.sql.ResultSet rs = ps.executeQuery();
            if (rs.next() && rs.getObject("id_propietario") == null) {
                mostrarError("La casa " + numeroCasa + " no tiene propietario registrado.\n"
                           + "No se puede registrar un pago para una casa deshabitada.");
                return;
            }
        } catch (java.sql.SQLException e) {
            mostrarError("Error al verificar la casa: " + e.getMessage());
            return;
        }

        // 1. Validar pago duplicado
        String sqlCheck = "SELECT COUNT(*) FROM Pagos WHERE numero_casa = ? AND mes = ? AND año = ?";
        try (java.sql.Connection con = ConexionDB.getConexion();
             java.sql.PreparedStatement ps = con.prepareStatement(sqlCheck)) {
            ps.setInt(1, numeroCasa);
            ps.setInt(2, numeroMes);
            ps.setInt(3, anio);
            java.sql.ResultSet rs = ps.executeQuery();
            if (rs.next() && rs.getInt(1) > 0) {
                mostrarError("La casa " + numeroCasa + " ya tiene registrado el pago de "
                        + mesSel + " " + anio + ".\nNo se permiten pagos duplicados.");
                return;
            }
        } catch (java.sql.SQLException e) {
            mostrarError("Error al verificar pago: " + e.getMessage());
            return;
        }

        // 2. Validar secuencia de meses
        if (!validarSecuenciaMeses(numeroCasa, numeroMes, anio, meses)) {
            return;
        }

        // 3. Obtener el propietario y su correo. Si no tiene correo registrado,
        //    no se registra el pago (el aviso es obligatorio).
        EnviarCorreo.Propietario prop = EnviarCorreo.propietarioPorCasa(numeroCasa);
        if (prop == null) {
            mostrarError("La casa " + numeroCasa + " no tiene un correo registrado.\n"
                       + "No se puede registrar el pago sin enviar la confirmación.");
            return;
        }

        // 4. Registrar el pago dentro de una transacción: solo se confirma si el
        //    correo se envía correctamente; si falla, se revierte.
        String sqlInsert = "INSERT INTO Pagos (numero_casa, mes, año, monto) VALUES (?, ?, ?, ?)";
        try (java.sql.Connection con = ConexionDB.getConexion()) {
            con.setAutoCommit(false);
            try (java.sql.PreparedStatement ps = con.prepareStatement(sqlInsert)) {
                ps.setInt(1, numeroCasa);
                ps.setInt(2, numeroMes);
                ps.setInt(3, anio);
                double cuotaActual = Double.parseDouble(jLabel6.getText().replace("Q. ", "").replace(",", ""));
                ps.setDouble(4, cuotaActual);
                ps.executeUpdate();

                String asunto = "Confirmación de pago - Casa " + numeroCasa;
                String cuerpo = "Estimado(a) " + prop.nombre + ",\n\n"
                              + "Se ha registrado exitosamente su pago:\n\n"
                              + "  Casa:   " + numeroCasa + "\n"
                              + "  Mes:    " + mesSel + " " + anio + "\n"
                              + "  Monto:  " + jLabel6.getText() + "\n\n"
                              + "Gracias por estar al día con sus cuotas.\n\n"
                              + "Atentamente,\n" + EmailConfig.NOMBRE_REMITENTE;

                try {
                    EnviarCorreo.enviar(prop.correo, asunto, cuerpo);
                } catch (javax.mail.MessagingException me) {
                    con.rollback();
                    mostrarError("El pago no se registró porque no se pudo enviar el "
                               + "correo de confirmación a " + prop.correo + ":\n"
                               + me.getMessage());
                    return;
                }

                con.commit();
            } catch (java.sql.SQLException e) {
                con.rollback();
                throw e;
            }

            javax.swing.JOptionPane.showMessageDialog(this,
                    "Pago registrado exitosamente.\n\n"
                  + "  Casa:   " + numeroCasa + "\n"
                  + "  Mes:    " + mesSel + " " + anio + "\n"
                  + "  Monto:  " + jLabel6.getText() + "\n\n"
                  + "Se envió la confirmación a " + prop.correo,
                    "Pago exitoso",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);

            limpiarFormulario();

        } catch (java.sql.SQLException e) {
            mostrarError("Error al registrar pago: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    
