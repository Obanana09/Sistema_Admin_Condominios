    /**
 * Obtiene desde la base de datos el valor actual
 * de la cuota mensual y lo muestra en pantalla.
 *
 * Si ocurre algún error durante la consulta,
 * se muestra el valor predeterminado de Q.1,500.00.
 */
    private void mostrarMonto() {
    String sql = "SELECT cuota FROM Configuracion WHERE id = 1";
    try (java.sql.Connection con = ConexionDB.getConexion();
         java.sql.Statement st = con.createStatement();
         java.sql.ResultSet rs = st.executeQuery(sql)) {
        if (rs.next()) {
            double cuota = rs.getDouble("cuota");
            jLabel6.setText("Q. " + String.format("%,.2f", cuota));
        }
    } catch (java.sql.SQLException e) {
        jLabel6.setText("Q. 1,500.00");
    }
}
