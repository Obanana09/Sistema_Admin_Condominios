    /**
 * Evento ejecutado al presionar el botón "Actualizar".
 *
 * Realiza las siguientes acciones:
 *
 * 1. Valida que el campo no esté vacío.
 * 2. Verifica que el valor ingresado sea numérico.
 * 3. Comprueba que la cuota sea mayor a cero.
 * 4. Solicita confirmación al administrador.
 * 5. Actualiza el valor en la base de datos.
 * 6. Actualiza la cuota vigente en memoria.
 * 7. Envía correos de notificación a los propietarios.
 * 8. Muestra un resumen del proceso realizado.
 *
 * @param evt Evento generado por el botón.
 */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nuevaCuotaStr = jTextField1.getText().trim();

        // 1. Validar que no esté vacío
        if (nuevaCuotaStr.isEmpty()) {
            mostrarError("Ingresa el monto de la nueva cuota.");
            jTextField1.requestFocus();
            return;
        }

        // 2. Validar que sea número válido
        double nuevaCuota;
        try {
            nuevaCuota = Double.parseDouble(nuevaCuotaStr.replace(",", "."));
        } catch (NumberFormatException e) {
            mostrarError("El monto ingresado no es un número válido.\nEjemplo: 1500 o 1500.00");
            jTextField1.requestFocus();
            return;
        }

        // 3. Validar que sea positivo
        if (nuevaCuota <= 0) {
            mostrarError("El monto de la cuota debe ser mayor a cero.");
            jTextField1.requestFocus();
            return;
        }

        // 4. Confirmar el cambio
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this,
                "¿Está seguro de cambiar la cuota a Q. " + String.format("%,.2f", nuevaCuota) + "?\n"
              + "Este cambio solo aplicará a pagos futuros.",
                "Confirmar cambio",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.QUESTION_MESSAGE);

        if (confirmacion != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        // 5. Guardar en BD
        // TODO: BD — cuando tengas tabla Configuracion, usar:
        // UPDATE Configuracion SET cuota = ? WHERE id = 1
        // Por ahora solo actualizamos el label
        String sqlUpdate = "UPDATE Configuracion SET cuota = ? WHERE id = 1";
try (java.sql.Connection con = ConexionDB.getConexion();
     java.sql.PreparedStatement ps = con.prepareStatement(sqlUpdate)) {
    ps.setDouble(1, nuevaCuota);
    ps.executeUpdate();
    cuotaVigente = nuevaCuota;
    jLabel2.setText("Q. " + String.format("%,.2f", cuotaVigente));
} catch (java.sql.SQLException e) {
    mostrarError("Error al guardar cuota: " + e.getMessage());
    return;
}
        // 6. Notificar por correo a todos los propietarios con correo registrado
        int enviados = 0;
        int fallidos = 0;
        String asunto = "Actualización de cuota mensual";
        String sqlPropietarios = "SELECT nombre, correo FROM Propietarios "
                               + "WHERE correo IS NOT NULL AND correo <> ''";
        try (java.sql.Connection con = ConexionDB.getConexion();
             java.sql.PreparedStatement ps = con.prepareStatement(sqlPropietarios);
             java.sql.ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                String nombre = rs.getString("nombre");
                String correo = rs.getString("correo");
                String cuerpo = "Estimado(a) " + nombre + ",\n\n"
                              + "Le informamos que la cuota mensual ha sido actualizada:\n\n"
                              + "  Nueva cuota: Q. " + String.format("%,.2f", nuevaCuota) + "\n\n"
                              + "Este cambio aplica solo a pagos futuros.\n\n"
                              + "Atentamente,\n" + EmailConfig.NOMBRE_REMITENTE;
                try {
                    EnviarCorreo.enviar(correo, asunto, cuerpo);
                    enviados++;
                } catch (javax.mail.MessagingException me) {
                    fallidos++;
                }
            }
        } catch (java.sql.SQLException e) {
            mostrarError("La cuota se actualizó, pero falló la consulta de "
                       + "propietarios para notificar: " + e.getMessage());
            jTextField1.setText("");
            return;
        }

        javax.swing.JOptionPane.showMessageDialog(this,
                "Cuota actualizada exitosamente.\n"
              + "Nueva cuota: Q. " + String.format("%,.2f", nuevaCuota) + "\n"
              + "El cambio aplica solo a pagos futuros.\n\n"
              + "Correos enviados: " + enviados + "\n"
              + "Correos fallidos: " + fallidos,
                "Actualización exitosa",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);

        jTextField1.setText("");
    }

