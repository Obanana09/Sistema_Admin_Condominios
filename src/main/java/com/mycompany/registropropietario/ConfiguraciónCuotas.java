    /**
 * Obtiene la cuota vigente desde la base de datos.
 *
 * Consulta la tabla Configuracion para recuperar
 * el valor actual de la cuota mensual y mostrarlo
 * en la interfaz gráfica.
 *
 * En caso de error se muestra un mensaje informativo.
 */
    private void cargarCuotaActual() {
        // TODO: BD — cuando tengas tabla de configuración, leer de ahí
        // Por ahora muestra el valor fijo
        String sql = "SELECT cuota FROM Configuracion WHERE id = 1";
    try (java.sql.Connection con = ConexionDB.getConexion();
         java.sql.Statement st = con.createStatement();
         java.sql.ResultSet rs = st.executeQuery(sql)) {
        if (rs.next()) {
            cuotaVigente = rs.getDouble("cuota");
            jLabel2.setText("Q. " + String.format("%,.2f", cuotaVigente));
        }
    } catch (java.sql.SQLException e) {
        mostrarError("Error al cargar cuota: " + e.getMessage());
    }
    }

