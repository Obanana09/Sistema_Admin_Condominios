 /**
 * Evento ejecutado al presionar el botón "Iniciar sesión".
 *
 * Realiza las siguientes acciones:
 * 1. Obtiene el usuario y contraseña ingresados.
 * 2. Verifica que los campos no estén vacíos.
 * 3. Compara los datos con las credenciales registradas.
 * 4. Permite el acceso al sistema si son correctos.
 * 5. Muestra mensajes de error si son incorrectos.
 *
 * @param evt Evento generado por el botón.
 */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String usuario = jTextField1.getText().trim();
    String password = new String(jPasswordField1.getPassword());

    if (usuario.isEmpty() || usuario.equals("Ingrese su usuario") || password.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Por favor ingresa usuario y contraseña.",
                "Campos vacíos",
                javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (usuario.equals(USUARIO) && password.equals(PASSWORD)) {
        new MenuInicio().setVisible(true);
        this.dispose();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this,
                "Usuario o contraseña incorrectos.\nIntenta de nuevo.",
                "Error de autenticación",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        jPasswordField1.setText("");
        jPasswordField1.requestFocus();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

