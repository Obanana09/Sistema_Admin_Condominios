    /**
     * Agrega un texto de ayuda (placeholder) a un campo de texto.
     *
     * Cuando el usuario hace clic en el campo, el texto desaparece.
     * Si el usuario abandona el campo vacío, el texto vuelve a mostrarse.
     *
     * @param field Campo de texto donde se colocará el placeholder.
     * @param placeholder Texto informativo que se mostrará.
     */
    private void addPlaceholder(JTextField field, String placeholder) {
    field.setText(placeholder);
    field.setForeground(Color.GRAY);
    field.addFocusListener(new FocusAdapter() {
        
        /**
        * Se ejecuta cuando el usuario selecciona el campo.
        */
        @Override
        public void focusGained(FocusEvent e) {
            if (field.getText().equals(placeholder)) {
                field.setText("");
                field.setForeground(field.getParent().getForeground()); // color del tema
            }
            
        /**
        * Se ejecuta cuando el usuario abandona el campo.
        */
        }
        @Override
        public void focusLost(FocusEvent e) {
            if (field.getText().isEmpty()) {
                field.setText(placeholder);
                field.setForeground(Color.GRAY);
            }
        }
    });
}
    
    

